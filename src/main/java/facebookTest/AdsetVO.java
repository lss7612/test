  public AdsetVO(Map<String, Object> rawData) {
    fromMap(rawData);
  }

  /**
   * 페이스북 그래프 api 호출후 받은 로우데이터(맵)에서 데이터 추출
   * @author 신종우
   * @param rawData 그래프 api 호출후 받은 로우데이터(맵)
   */
  @SuppressWarnings("unchecked")
  public void fromMap(Map<String, Object> rawData) {
    /* Adset Inshight 정보 */
    List<Map<String, Object>> actions = (List<Map<String, Object>>) rawData.get("actions");
    List<Map<String, Object>> action_values = (List<Map<String, Object>>) rawData.get("action_values");
    
    this.campaign_id = (String)rawData.get("campaign_id");
    this.adset_id = (String)rawData.get("adset_id");
    this.exposure_cnt = (String)rawData.get("impressions");
    this.click_cnt = (String)rawData.get("clicks");
    this.acc_start_dt = (String)rawData.get("date_start");
    this.acc_end_dt = (String)rawData.get("date_start");
    //23846769567170471/insights?level=adset&fields=account_id,campaign_name,campaign_id,adset_name,adset_id,impressions,clicks,actions,action_values,spend&filtering=[{field:'action_type',operator:'IN',value:['offsite_conversion.fb_pixel_purchase','offsite_conversion.fb_pixel_complete_registration']}]&limit=100&time_increment=1
    try {
      this.sent_adcost_amt = Integer.parseInt((String)rawData.get("spend"));
    } catch(Exception ignored) {}
    
    try {
      for (int i = 0; i < actions.size(); i++) {
        switch((String)actions.get(i).get("action_type")) {
        case "offsite_conversion.fb_pixel_purchase":
          this.purchase_cnt = Integer.parseInt((String)actions.get(i).get("value"));
          this.sales_amt = Integer.parseInt((String)action_values.get(i).get("value"));
          break;
        case "offsite_conversion.fb_pixel_complete_registration":
          this.registration_cnt = Integer.parseInt((String)actions.get(i).get("value"));
          break;
        }
      }
    } catch(Exception ignored) {}
    
    
    if (sent_adcost_amt != 0) {
      this.roas = (double)sales_amt/sent_adcost_amt*100;
    }
    
  }
